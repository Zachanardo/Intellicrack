_logger = get_logger("core.script_gen")

ScriptType = Literal["frida", "ghidra", "radare2", "python"]


class BypassStrategy(Enum):
    """Bypass strategy types for license protections."""

    RETURN_TRUE = "return_true"
    RETURN_FALSE = "return_false"
    RETURN_ZERO = "return_zero"
    RETURN_ONE = "return_one"
    NOP_FUNCTION = "nop_function"
    SKIP_CHECK = "skip_check"
    PATCH_JUMP = "patch_jump"
    HOOK_REPLACE = "hook_replace"
    MEMORY_PATCH = "memory_patch"


@dataclass
class ScriptTemplate:
    """Template for script generation.

    Attributes:
        name: Template name.
        script_type: Type of script.
        description: Template description.
        template: Template string with substitution markers.
        parameters: Required parameters.
    """

    name: str
    script_type: ScriptType
    description: str
    template: str
    parameters: list[str]


@dataclass
class GeneratedScript:
    """A generated script ready for execution.

    Attributes:
        name: Script name.
        script_type: Type of script.
        content: Script content.
        description: Description of what the script does.
        created_at: Generation timestamp.
        parameters_used: Parameters used in generation.
        target_functions: Target functions the script operates on.
    """

    name: str
    script_type: ScriptType
    content: str
    description: str
    created_at: datetime = field(default_factory=datetime.now)
    parameters_used: dict[str, Any] = field(default_factory=dict)
    target_functions: list[str] = field(default_factory=list)

    def save(self, path: Path) -> None:
        """Save script to file.

        Args:
            path: File path to save to.
        """
        path.parent.mkdir(parents=True, exist_ok=True)
        path.write_text(self.content, encoding="utf-8")
        _logger.info("Saved script to %s", path)


