        """Generate a memory pattern scanner script.

        Args:
            patterns: List of (name, hex_pattern) tuples.
            module_filter: Optional module name to limit search.

        Returns:
            Generated Frida memory scanner.
        """
        import json

        content = textwrap.dedent(f'''
            // Memory Pattern Scanner
            // Generated by Intellicrack at {datetime.now().isoformat()}

            "use strict";

            const patterns = {json.dumps([{"name": n, "pattern": p} for n, p in patterns])};
            const moduleFilter = {json.dumps(module_filter)};

            function scanPattern(pattern, ranges) {{
                const matches = [];
                for (const range of ranges) {{
                    try {{
                        const results = Memory.scanSync(range.base, range.size, pattern);
                        matches.push(...results);
                    }} catch (e) {{
                        // Skip inaccessible ranges
                    }}
                }}
                return matches;
            }}

            function main() {{
                console.log("[*] Intellicrack Memory Scanner");

                let ranges;
                if (moduleFilter) {{
                    const module = Process.findModuleByName(moduleFilter);
                    if (!module) {{
                        console.error(`[-] Module not found: ${{moduleFilter}}`);
                        return;
                    }}
                    ranges = [{{ base: module.base, size: module.size }}];
                }} else {{
                    ranges = Process.enumerateRanges("r--");
                }}

                for (const p of patterns) {{
                    console.log(`\\n[*] Scanning for: ${{p.name}}`);
                    console.log(`    Pattern: ${{p.pattern}}`);

                    const matches = scanPattern(p.pattern, ranges);

                    if (matches.length === 0) {{
                        console.log("    No matches found");
                    }} else {{
                        console.log(`    Found ${{matches.length}} matches:`);
                        for (const m of matches) {{
                            console.log(`      ${{m.address}}`);
                        }}
                    }}
                }}
            }}

            main();
        ''').strip()

        return GeneratedScript(
            name=f"memory_scanner_{datetime.now().strftime('%Y%m%d_%H%M%S')}",
            script_type="frida",
            content=content,
            description="Memory pattern scanner",
            parameters_used={
                "patterns": patterns,
                "module_filter": module_filter,
            },
        )
