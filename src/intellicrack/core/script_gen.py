    def generate_patch_commands(
        self,
        binary_path: Path,
        patch: PatchInfo,
    ) -> GeneratedScript:
        """Generate radare2 patch commands.

        Args:
            binary_path: Path to the binary.
            patch: Patch information.

        Returns:
            Generated radare2 commands.
        """
        original_hex = patch.original_bytes.hex()
        new_hex = patch.new_bytes.hex()

        if len(patch.new_bytes) < len(patch.original_bytes):
            nop_count = len(patch.original_bytes) - len(patch.new_bytes)
            new_hex += "90" * nop_count

        patch_commands = f"wx {new_hex}"

        return self.generate_from_template(
            "radare2_patch_jump",
            {
                "binary_path": str(binary_path),
                "patch_address": f"0x{patch.address:X}",
                "original_instruction": original_hex,
                "patch_type": patch.description,
                "patch_commands": patch_commands,
            },
        )

    def generate_keygen_template(
        self,
        algorithm_name: str,
        key_length: int = 16,
        checksum_type: str = "sum",
    ) -> GeneratedScript:
        """Generate a Python keygen template.

        Args:
            algorithm_name: Name for the keygen.
            key_length: Length of generated keys.
            checksum_type: Type of checksum validation.

        Returns:
            Generated Python keygen template.
        """
        content = textwrap.dedent(f'''
            #!/usr/bin/env python3
            """
            Keygen for {algorithm_name}
            Generated by Intellicrack at {datetime.now().isoformat()}

            This is a template - implement the actual algorithm based on analysis.
            """

            import hashlib
            import random
            import string
            from typing import Optional


            class {algorithm_name.replace(" ", "")}Keygen:
                """Keygen implementation for {algorithm_name}."""

                KEY_LENGTH = {key_length}
                CHECKSUM_TYPE = "{checksum_type}"

                # Character set for key generation
                CHARSET = string.ascii_uppercase + string.digits

                def __init__(self, name: str = "") -> None:
                    """Initialize keygen.

                    Args:
                        name: Optional registration name.
                    """
                    self.name = name

                def generate(self) -> str:
                    """Generate a valid license key.

                    Returns:
                        Generated license key.
                    """
                    # Generate key parts using standard serial format
                    # Customize the algorithm after analyzing the target binary
                    key_parts = []

                    for _ in range(4):
                        part = "".join(random.choices(self.CHARSET, k=4))
                        key_parts.append(part)

                    key = "-".join(key_parts)

                    if self.CHECKSUM_TYPE == "sum":
                        key = self._add_checksum_sum(key)
                    elif self.CHECKSUM_TYPE == "xor":
                        key = self._add_checksum_xor(key)

                    return key

                def _add_checksum_sum(self, key: str) -> str:
                    """Add sum-based checksum.

                    Args:
                        key: Key without checksum.

                    Returns:
                        Key with checksum appended.
                    """
                    checksum = sum(ord(c) for c in key.replace("-", "")) % 256
                    return f"{{key}}-{{checksum:02X}}"

                def _add_checksum_xor(self, key: str) -> str:
                    """Add XOR-based checksum.

                    Args:
                        key: Key without checksum.

                    Returns:
                        Key with checksum appended.
                    """
                    checksum = 0
                    for c in key.replace("-", ""):
                        checksum ^= ord(c)
                    return f"{{key}}-{{checksum:02X}}"

                def validate(self, key: str) -> bool:
                    """Validate a license key.

                    Args:
                        key: Key to validate.

                    Returns:
                        True if key is valid.
                    """
                    # Validate key format and checksum
                    # Extend with target-specific validation after analysis
                    if not key or len(key) < self.KEY_LENGTH:
                        return False

                    # Verify standard serial format
                    parts = key.split("-")
                    if len(parts) < 4:
                        return False

                    # Verify checksum if present
                    if len(parts) == 5:
                        key_body = "-".join(parts[:4])
                        expected_checksum = parts[4]
                        if self.CHECKSUM_TYPE == "sum":
                            calc = sum(ord(c) for c in key_body.replace("-", "")) % 256
                            if f"{{calc:02X}}" != expected_checksum:
                                return False
                        elif self.CHECKSUM_TYPE == "xor":
                            calc = 0
                            for c in key_body.replace("-", ""):
                                calc ^= ord(c)
                            if f"{{calc:02X}}" != expected_checksum:
                                return False

                    return True


            def main() -> None:
                """Main entry point."""
                print(f"{{algorithm_name}} Keygen")
                print("=" * 40)

                keygen = {algorithm_name.replace(" ", "")}Keygen()

                for i in range(5):
                    key = keygen.generate()
                    valid = keygen.validate(key)
                    status = "VALID" if valid else "INVALID"
                    print(f"Key {{i+1}}: {{key}} [{{status}}]")


            if __name__ == "__main__":
                main()
        ''').strip()

        return GeneratedScript(
            name=f"keygen_{algorithm_name.lower().replace(' ', '_')}",
            script_type="python",
            content=content,
            description=f"Keygen template for {algorithm_name}",
            parameters_used={
                "algorithm_name": algorithm_name,
                "key_length": key_length,
                "checksum_type": checksum_type,
            },
        )

