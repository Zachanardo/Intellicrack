    def register_template(self, template: ScriptTemplate) -> None:
        """Register a custom script template.

        Args:
            template: Template to register.
        """
        self._templates[template.name] = template
        _logger.info("Registered template: %s", template.name)

    def get_template(self, name: str) -> ScriptTemplate | None:
        """Get a template by name.

        Args:
            name: Template name.

        Returns:
            Template or None if not found.
        """
        return self._templates.get(name)

    def list_templates(self, script_type: ScriptType | None = None) -> list[str]:
        """List available templates.

        Args:
            script_type: Optional filter by script type.

        Returns:
            List of template names.
        """
        if script_type is None:
            return list(self._templates.keys())
        return [
            name
            for name, tmpl in self._templates.items()
            if tmpl.script_type == script_type
        ]

    def generate_from_template(
        self,
        template_name: str,
        parameters: dict[str, Any],
    ) -> GeneratedScript:
        """Generate a script from a template.

        Args:
            template_name: Name of template to use.
            parameters: Template parameters.

        Returns:
            Generated script.

        Raises:
            ValueError: If template not found or parameters missing.
        """
        template = self._templates.get(template_name)
        if template is None:
            raise ValueError(f"Template not found: {template_name}")

        parameters["timestamp"] = datetime.now().isoformat()

        missing = [p for p in template.parameters if p not in parameters]
        if missing:
            raise ValueError(f"Missing parameters: {missing}")

        content = template.template
        for key, value in parameters.items():
            marker = "{" + key + "}"
            if isinstance(value, str):
                content = content.replace(marker, value)
            else:
                content = content.replace(marker, str(value))

        return GeneratedScript(
            name=f"{template_name}_{datetime.now().strftime('%Y%m%d_%H%M%S')}",
            script_type=template.script_type,
            content=content,
            description=template.description,
            parameters_used=parameters,
        )

    def generate_hook_script(
        self,
        function: FunctionInfo,
        module_name: str,
        strategy: BypassStrategy,
        custom_code: str | None = None,
    ) -> GeneratedScript:
        """Generate a Frida hook script for a function.

        Args:
            function: Target function information.
            module_name: Name of the module containing the function.
            strategy: Bypass strategy to apply.
            custom_code: Optional custom code to inject.

        Returns:
            Generated Frida script.
        """
        return_value = self._get_return_value_for_strategy(strategy)
        on_enter = custom_code or "// No custom enter code"

        return self.generate_from_template(
            "frida_hook_return",
            {
                "module_name": module_name,
                "function_name": function.name,
                "function_offset": f"0x{function.address:X}",
                "return_value": return_value,
                "on_enter_code": on_enter,
            },
        )

    def _get_return_value_for_strategy(self, strategy: BypassStrategy) -> str:
        """Get return value string for a bypass strategy.

        Args:
            strategy: Bypass strategy.

        Returns:
            JavaScript return value expression.
        """
        strategy_values: dict[BypassStrategy, str] = {
            BypassStrategy.RETURN_TRUE: "1",
            BypassStrategy.RETURN_FALSE: "0",
            BypassStrategy.RETURN_ZERO: "0",
            BypassStrategy.RETURN_ONE: "1",
            BypassStrategy.NOP_FUNCTION: "0",
            BypassStrategy.SKIP_CHECK: "1",
            BypassStrategy.HOOK_REPLACE: "1",
            BypassStrategy.PATCH_JUMP: "1",
            BypassStrategy.MEMORY_PATCH: "1",
        }
        return strategy_values.get(strategy, "0")

    def generate_bypass_script(
        self,
        binary_name: str,
        module_name: str,
        functions: list[tuple[str, BypassStrategy]],
        patches: list[PatchInfo] | None = None,
    ) -> GeneratedScript:
        """Generate a comprehensive bypass script.

        Args:
            binary_name: Name of the target binary.
            module_name: Name of the target module.
            functions: List of (function_name, strategy) tuples.
            patches: Optional list of memory patches to apply.

        Returns:
            Generated Frida bypass script.
        """
        import json

        bypass_functions = [
            {
                "name": func_name,
                "returnValue": int(self._get_return_value_for_strategy(strategy)),
            }
            for func_name, strategy in functions
        ]

        patch_addresses = []
        if patches:
            for patch in patches:
                patch_addresses.append({
                    "offset": f"0x{patch.address:X}",
                    "bytes": list(patch.new_bytes),
                })

        return self.generate_from_template(
            "frida_bypass_license",
            {
                "binary_name": binary_name,
                "module_name": module_name,
                "bypass_functions_json": json.dumps(bypass_functions),
                "patch_addresses_json": json.dumps(patch_addresses),
            },
        )

